function [pts, ptsLine] = digitizedSmallBistratifiedDensity_Dacey1993_Fig13D()

    % Dacey (1993) Morphology of a small-bistratified ganglion cell type in
    % the macaque and human retina. Visual Neuroscience, 10(6), 1081-1098

% Figure 13D - small bistratified RGC proportion of total RGCs in macaque

    [pts, ptsLine] = getData();

    figure(); hold on;
    plot(ptsLine(:,1), ptsLine(:,2),...
        'Color', [0.75 0.75 1], 'LineWidth', 2);
    plot(pts(:,1), pts(:,2), 'ob',...
        'MarkerFaceColor', 'b');
    xlim([0 12]); ylim([0 10]);
    legend('Small bistratified RGC', 'Location', 'northwest');
    xlabel('Eccentricity (mm)'); % Temporal
    ylabel('Proportion of RGCs (%)');
    grid on; axis square;
    tightfig(gcf);
end

